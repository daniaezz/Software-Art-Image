//--------------------------------------------------------------
void ofApp::draw(){
    rows = 5;
    cols = 5;
    for( int i=0; i<rows; i++){
        for(int j=0; j<cols; j++){
            ofNoFill();
            ofSetColor(255);
            ofSetLineWidth(5);
            
            ofDrawRectangle(j*ofGetWidth()/cols, i*ofGetHeight()/rows, ofGetWidth()/cols, ofGetHeight()/rows);
        }
    }

}

